package Helper;

public class TwelveDaysHelper
{
    String day;
    String verse;
    public TwelveDaysHelper(String day, String verse)
    {
     this.day=day;
     this.verse=verse;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getVerse() {
        return verse;
    }

    public void setVerse(String verse) {
        this.verse = verse;
    }
}
